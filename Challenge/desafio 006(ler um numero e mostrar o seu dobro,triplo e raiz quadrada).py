n = int(input('Digite um número :')) # Digite um número para calcular seu dobro,triplo e raiz quadrada
d  = n * 2 # Operação para calcular o dobro
t  = n * 3 # Operação para calcullar o triplo
r  = n ** (1/2) # Operação para calcular a raiz quadrada
print('Seu dobro vale {}'.format(d)) # Mostrará o dobro
print('Seu triplo vale {}'.format(t)) # Mostrará o triplo
print('E sua raiz quadrada equivale {}'.format(r)) # Mostrará a raiz quadrada
